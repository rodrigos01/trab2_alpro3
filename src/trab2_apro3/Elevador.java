package trab2_apro3;

/**
 *
 * @author rodrigo
 */
public class Elevador {
    
    private int base, passo;

    public Elevador(int base, int passo) {
        this.base = base;
        this.passo = passo;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getPasso() {
        return passo;
    }

    public void setPasso(int passo) {
        this.passo = passo;
    }
    
    
    
}
